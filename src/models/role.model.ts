// ####################################################################################################
// ## IMPORTACIÓNS
// ####################################################################################################
import { Collection, Entity, Property, ManyToMany, Enum, Unique } from '@mikro-orm/core';
import { BaseEntity } from "./base-entity.model";
import { Permissions } from "./permissions.model";

// ####################################################################################################
// ## ENUMS
// ####################################################################################################
export enum Scope {
    PERFORMANCE,
    PROJECT,
    REQUIREMENT,
    STAGE,
    STATE,
    SYSTEM,
}

// ####################################################################################################
// ## CLASE Role
// ####################################################################################################
@Entity()
export class Role extends BaseEntity {
    // ************************************************************************************************
    // ** ATRIBUTOS
    // ************************************************************************************************
    @Property()
    @Unique()
    public name         : string;
    @Property()
    public description  : string;
    @Property()
    @Enum({ items: () => Scope, array: true, nullable: false })
    public scope   : Scope[];

    // Relacións
    @ManyToMany(() => Permissions)
    public permissions  : Collection<Permissions> = new Collection<Permissions>(this);

    // ************************************************************************************************
    // ** CONSTRUTOR
    // ************************************************************************************************
    constructor(obj?: Partial<Role>) {
        super();
        Object.assign(this, obj);
    }

    // ************************************************************************************************
    // ** MÉTODOS
    // ************************************************************************************************
}
